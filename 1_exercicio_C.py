
# 1- Dada as idades I, J, K, X, Y. Faça um algoritmo para calcular a média das idades.

i = int(input("Digite a idade da PRIMEIRA pessoa: "))
j = int(input("Digite a idade da SEGUNDA pessoa: "))
k = int(input("Digite a idade da TERCEIRA pessoa: "))
x = int(input("Digite a idade da QUARTA pessoa: "))
y = int(input("Digite a idade da QUINTA pessoa: "))

media = int(i + j + k + x + y)/5

print("A média de idades é de:", int(media), "anos!")
